    public EnglishHtmlTranslationMiddleware(RequestDelegate next)
    {
        _next = next;
    }

    public async Task InvokeAsync(HttpContext context)
    {
        if (!AppLanguage.IsEnglish(context))
        {
            await _next(context);
            return;
        }

        var originalBody = context.Response.Body;
        await using var buffer = new MemoryStream();
        context.Response.Body = buffer;

        try
        {
            await _next(context);

            var contentType = context.Response.ContentType ?? string.Empty;
            if (!contentType.Contains("text/html", StringComparison.OrdinalIgnoreCase))
            {
                buffer.Position = 0;
                await buffer.CopyToAsync(originalBody);
                return;
            }

            buffer.Position = 0;
            using var reader = new StreamReader(buffer, Encoding.UTF8, detectEncodingFromByteOrderMarks: true, leaveOpen: true);
            var html = await reader.ReadToEndAsync();
            html = Translate(html);

            var bytes = Encoding.UTF8.GetBytes(html);
            context.Response.ContentLength = bytes.Length;
            await originalBody.WriteAsync(bytes);
        }
        finally
        {
            context.Response.Body = originalBody;
        }
    }

    private static string Translate(string html)
    {
        foreach (var pair in Translations.OrderByDescending(item => item.Key.Length))
        {
            if (WordTranslations.ContainsKey(pair.Key))
            {
                continue;
            }

            html = html.Replace(pair.Key, pair.Value, StringComparison.Ordinal);
        }

        foreach (var pair in WordTranslations.OrderByDescending(item => item.Key.Length))
        {
            var pattern = $@"(?<![\p{{L}}\p{{N}}]){Regex.Escape(pair.Key)}(?![\p{{L}}\p{{N}}])";
            html = Regex.Replace(html, pattern, pair.Value);
        }

        return NormalizeRenderedEnglish(html);
    }

    private static string NormalizeRenderedEnglish(string html)
    {
        html = Regex.Replace(html, @"Admin/Manager\s+login[ıi]?\s+completed\.", "Admin/Manager signed in.", RegexOptions.IgnoreCase);
        html = Regex.Replace(html, @"Admin/Manager\s+login[ıi]?\s+yap[ıi]ld[ıi]\.", "Admin/Manager signed in.", RegexOptions.IgnoreCase);
        html = Regex.Replace(html, @"\blogin[ıi]?\s+completed\.", "signed in.", RegexOptions.IgnoreCase);
        html = Regex.Replace(html, @"\blogin[ıi]?\s+yap[ıi]ld[ıi]\.", "signed in.", RegexOptions.IgnoreCase);
        html = Regex.Replace(html, @"\b[çc]imento\b", "cement", RegexOptions.IgnoreCase);
        html = Regex.Replace(html, @"\bAccountingci\b", "Accounting", RegexOptions.IgnoreCase);
        html = Regex.Replace(html, @"\(1\s+kisi\)", "(1 person)", RegexOptions.IgnoreCase);
        html = Regex.Replace(html, @"\((\d+)\s+kisi\)", "($1 people)", RegexOptions.IgnoreCase);
        return html;
    }
